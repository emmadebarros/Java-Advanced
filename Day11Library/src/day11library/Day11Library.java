    private void reloadFromDatabase() {
        try {
            ArrayList<Book> list = db.getAllBooks();
            libraryListModel.clear(); // remove any items that may be in model
            for (Book b : list) {
                libraryListModel.addElement(b);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Failed to fetch records: " + ex.getMessage(),
                    "Database error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

